package hw9df;

public class Kid {
    private String name;
    public String lastName;
    private int year;
    private String group;

    public Kid(String name, String lastName, int year, String group) {
        this.name = name;
        this.lastName = lastName;
        this.year = year;
        this.group = group;
    }

    public String getName() {
        return name = name;
    }

    public String getGroup() {
        return group = group;
    }
}

