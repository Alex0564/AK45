package homework10;

public class Coefficients {
    protected int a;
    protected int b;

    public Coefficients() {
    }

    public Coefficients(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getA() {
        return a;
    }
    public void setB(int b) {
        this.b = b;
    }
    public int getB() {
        return b;
    }

}
